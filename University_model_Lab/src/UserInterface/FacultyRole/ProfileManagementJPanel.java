    private void loadProfile() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        // Add current faculty profile to table
        model.addRow(new Object[]{
            faculty.getPerson().getFullName(),
            faculty.getPerson().getPersonId(),
            faculty.getPerson().getEmail() != null ? faculty.getPerson().getEmail() : "",
            faculty.getPerson().getPhone() != null ? faculty.getPerson().getPhone() : "",
            faculty.getDepartment() != null ? faculty.getDepartment() : "",
            faculty.getOfficeLocation() != null ? faculty.getOfficeLocation() : ""
        });
        
        // Populate fields with current data
        fieldFullName.setText(faculty.getPerson().getFullName());
        fieldFaculty.setText(faculty.getPerson().getPersonId());
        fieldEmail.setText(faculty.getPerson().getEmail() != null ? faculty.getPerson().getEmail() : "");
        fieldPhoneNumber.setText(faculty.getPerson().getPhone() != null ? faculty.getPerson().getPhone() : "");
        fieldDepartment.setText(faculty.getDepartment() != null ? faculty.getDepartment() : "");
        fieldLocation.setText(faculty.getOfficeLocation() != null ? faculty.getOfficeLocation() : "");
    }
    
