    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        String message = "Full Name: " + faculty.getPerson().getFullName() + "\n" +
            "Faculty ID: " + faculty.getPerson().getPersonId() + "\n" +
            "Email: " + (faculty.getPerson().getEmail() != null ? faculty.getPerson().getEmail() : "N/A") + "\n" +
            "Phone: " + (faculty.getPerson().getPhone() != null ? faculty.getPerson().getPhone() : "N/A") + "\n" +
            "Department: " + (faculty.getDepartment() != null ? faculty.getDepartment() : "N/A") + "\n" +
            "Office Location: " + (faculty.getOfficeLocation() != null ? faculty.getOfficeLocation() : "N/A");
        
        javax.swing.JOptionPane.showMessageDialog(this, message, "Faculty Profile", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String email = fieldEmail.getText().trim();
        String phone = fieldPhoneNumber.getText().trim();
        String department = fieldDepartment.getText().trim();
        String location = fieldLocation.getText().trim();
        
        // Update faculty profile
        if (!email.isEmpty()) faculty.getPerson().setEmail(email);
        if (!phone.isEmpty()) faculty.getPerson().setPhone(phone);
        if (!department.isEmpty()) faculty.setDepartment(department);
        if (!location.isEmpty()) faculty.setOfficeLocation(location);
        
        javax.swing.JOptionPane.showMessageDialog(this, "Profile updated successfully!");
        loadProfile();
    }//GEN-LAST:event_btnUpdateActionPerformed

