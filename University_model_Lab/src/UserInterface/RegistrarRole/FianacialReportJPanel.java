    private void loadFinancialData() {
        DefaultTableModel model = (DefaultTableModel) tblFinance.getModel();
        model.setRowCount(0);
        
        double totalBilled = 0.0;
        double totalPaid = 0.0;
        double totalRefunded = 0.0;
        double totalOutstanding = 0.0;
        
        for (StudentProfile student : business.getStudentDirectory().getStudentList()) {
            double studentBilled = 0.0;
            double studentPaid = 0.0;
            double studentRefunded = 0.0;
            
            for (CourseEnrollment enrollment : student.getEnrollments()) {
                studentBilled += enrollment.getTuitionAmount();
                if (enrollment.isTuitionPaid()) {
                    studentPaid += enrollment.getTuitionAmount();
                }
            }
            
            double studentOutstanding = studentBilled - studentPaid - studentRefunded;
            
            if (studentBilled > 0) {
                model.addRow(new Object[]{
                    student.getPerson().getPersonId(),
                    String.format("$%.2f", studentBilled),
                    String.format("$%.2f", studentPaid),
                    String.format("$%.2f", studentRefunded),
                    String.format("$%.2f", studentOutstanding)
                });
                
                totalBilled += studentBilled;
                totalPaid += studentPaid;
                totalRefunded += studentRefunded;
                totalOutstanding += studentOutstanding;
            }
        }
        
        lblCollected.setText("Collected: $" + String.format("%.2f", totalPaid));
        lblUnpaid.setText("Unpaid: $" + String.format("%.2f", totalOutstanding));
    }
    
