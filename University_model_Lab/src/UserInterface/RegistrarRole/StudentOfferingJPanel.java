    private void loadOffers() {
        DefaultTableModel model = (DefaultTableModel) tblOffers.getModel();
        model.setRowCount(0);
        
        for (CourseOffer offer : business.getCourseOfferDirectory().getCourseOffersBySemester(currentSemester)) {
            String facultyId = offer.getAssignedFaculty() != null ? 
                offer.getAssignedFaculty().getPerson().getPersonId() : "Not Assigned";
            
            model.addRow(new Object[]{
                offer.getCourse().getCourseId(),
                offer.getCourse().getCourseName(),
                offer.getCapacity(),
                offer.getRoomLocation() != null ? offer.getRoomLocation() : "",
                offer.getSchedule() != null ? offer.getSchedule() : "",
                facultyId,
                offer.getEnrolledCount()
            });
        }
    }
    
