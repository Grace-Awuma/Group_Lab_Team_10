    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tblOffers.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a row");
            return;
        }
        
        String courseId = String.valueOf(tblOffers.getValueAt(row, 0));
        
        int capacity;
        try {
            capacity = Integer.parseInt(txtCapacity.getText().trim());
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter numeric capacity");
            return;
        }
        
        // Find and update the course offer
        CourseOffer targetOffer = null;
        for (CourseOffer offer : business.getCourseOfferDirectory().getCourseOffersBySemester(currentSemester)) {
            if (offer.getCourse().getCourseId().equals(courseId)) {
                targetOffer = offer;
                break;
            }
        }
        
        if (targetOffer == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Offer not found");
            return;
        }
        
        // Update capacity and details
        targetOffer.setCapacity(capacity);
        targetOffer.setRoomLocation(txtRoom.getText().trim());
        targetOffer.setSchedule(txtTime.getText().trim());
        
        javax.swing.JOptionPane.showMessageDialog(this, "Capacity/Room/Time updated");
        loadOffers();

    }//GEN-LAST:event_btnUpdateActionPerformed


