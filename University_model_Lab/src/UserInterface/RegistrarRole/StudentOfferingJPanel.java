    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:

        int row = tblOffers.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a row");
            return;
        }
        
        String courseId = String.valueOf(tblOffers.getValueAt(row, 0));
        String facultyId = txtFacultyId.getText().trim();
        
        if (facultyId.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter Faculty Person ID");
            return;
        }
        
        FacultyProfile faculty = business.getFacultyDirectory().findFaculty(facultyId);
        if (faculty == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Faculty not found: " + facultyId);
            return;
        }
        
        // Find and update the course offer
        for (CourseOffer offer : business.getCourseOfferDirectory().getCourseOffersBySemester(currentSemester)) {
            if (offer.getCourse().getCourseId().equals(courseId)) {
                offer.setAssignedFaculty(faculty);
                faculty.addAssignedCourse(offer);
                break;
            }
        }
        
        javax.swing.JOptionPane.showMessageDialog(this, "Assigned " + facultyId + " to " + courseId);
        loadOffers();
    }//GEN-LAST:event_btnAssignActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String courseId = txtCourseNum.getText().trim();
        String capStr = txtCapacity.getText().trim();
        
        if (courseId.isEmpty() || capStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Course # and Capacity required");
            return;
        }
        
        int capacity;
        try {
            capacity = Integer.parseInt(capStr);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Capacity must be a number");
            return;
        }
        
        // Find the course
        Course course = business.getCourseDirectory().findCourse(courseId);
        if (course == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Course not found: " + courseId);
            return;
        }
        
        // Create course offer
        String offerId = courseId + "-" + currentSemester.replace(" ", "") + "-" + System.currentTimeMillis();
        CourseOffer offer = business.getCourseOfferDirectory().newCourseOffer(offerId, course, currentSemester, capacity);
        offer.setRoomLocation(txtRoom.getText().trim());
        offer.setSchedule(txtTime.getText().trim());
        
        javax.swing.JOptionPane.showMessageDialog(this, "Offer created for " + courseId + " with " + capacity + " seats");
        loadOffers();

    }//GEN-LAST:event_btnCreateActionPerformed

